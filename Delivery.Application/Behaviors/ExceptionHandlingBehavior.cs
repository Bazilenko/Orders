using Delivery.Domain.Exceptions;
using MediatR;
using Microsoft.Extensions.Logging;
using FluentValidation;
public class ExceptionHandlingBehavior<TRequest, TResponse> : IPipelineBehavior<TRequest, TResponse>
    where TRequest : notnull
{
    private readonly ILogger<ExceptionHandlingBehavior<TRequest, TResponse>> _logger;

    public ExceptionHandlingBehavior(ILogger<ExceptionHandlingBehavior<TRequest, TResponse>> logger)
    {
        _logger = logger;
    }

    public async Task<TResponse> Handle(
        TRequest request,
        RequestHandlerDelegate<TResponse> next,
        CancellationToken cancellationToken)
    {
        try
        {
            return await next();
        }
        catch (DomainException dex)
        {
            _logger.LogWarning(dex,
                "Domain validation failed in {RequestName}",
                typeof(TRequest).Name);
            throw;
        }
        catch (ValidationException vex)
        {
            foreach (var failure in vex.Errors)
            {
                _logger.LogWarning(
                    vex,
                    "Validation failure in {RequestName}: Property {PropertyName}, Error: {ErrorMessage}",
                    typeof(TRequest).Name, failure.PropertyName, failure.ErrorMessage);
            }

            throw;
        }
        catch (Exception ex)
        {
            _logger.LogError(ex,
                "Unhandled exception in {RequestName}",
                typeof(TRequest).Name);
            throw;
        }
    }
}
